import math
ang = float(input('Digite o angulo que voce deseja: '))
seno = math.sin(ang)
cosseno = math.cos(ang)
tangente = math.tan(ang)
print('O angulo de {} tem seno de {}'.format(ang, seno))
print('O angulo de {} tem cosseno de {}'.format(ang, cosseno))
print('O angulo de {} tem tangente de {}'.format(ang, tangente))
